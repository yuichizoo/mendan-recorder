export interface HomonUserInput {
  patientId: string
  dateStr: string
  prevKarte: string
  segments: Segment[]
}

export function buildHomonUser(input: HomonUserInput): string {
  const memo = input.segments
    .filter((s) => s.text.trim())
    .map((s) => {
      const d = new Date(s.ts)
      const hh = String(d.getHours()).padStart(2, '0')
      const mm = String(d.getMinutes()).padStart(2, '0')
      return `[${hh}:${mm}] ${s.text.trim()}`
    })
    .join('\n')

  const prev = input.prevKarte.trim()
    ? input.prevKarte.trim()
    : '(前回カルテの貼り付けなし。ヘッダー部は作らず、SOAPのみ作成し、要確認リストに「前回カルテ未貼り付け」と記載すること)'

  return `# 訪問情報
- 実施日: ${input.dateStr}
- 患者ID: ${input.patientId || '(未入力)'}

# 前回カルテ
${prev}

# 当日メモ(時刻順セグメント)
${memo}

上記から当日の定期訪問カルテを作成してください。`
}

// 聞き漏らしチェック(訪問診療): この2〜3点があれば【要記入】がほぼ消える
const HOMON_CHECK_ITEMS = [
  '当日の所見・様子(「変わりなし」の一言でも可)',
  '処方・方針の変更の有無(「変更なし」でも可)',
  '次回の予定(日付とやること)',
  '本人・家族の発言(S欄の材料)',
]

export function buildHomonCheckSystem(): string {
  const list = HOMON_CHECK_ITEMS.map((i) => `- ${i}`).join('\n')
  return `あなたは訪問診療の記録漏れ防止アシスタントです。当日メモを読み、カルテ作成に必要な項目のうち「まだメモに含まれていない項目」だけを返します。

# 必須項目リスト
${list}

# 出力ルール
- 不足項目のみを「・項目名」の箇条書きで返す。1項目1行、補足は書かない。
- メモに少しでも具体的な言及があれば「含まれている」と判定する(厳しくしすぎない)。
- すべて揃っていれば「✓ 必須項目はすべて記録されています」とだけ返す。
- 前置き・後書き・説明は一切書かない。`
}

export function buildHomonCheckUser(segments: Segment[]): string {
  const memo = segments
    .filter((s) => s.text.trim())
    .map((s) => s.text.trim())
    .join('\n')
  return `# ここまでの当日メモ\n${memo}\n\n不足している必須項目を返してください。`
}
